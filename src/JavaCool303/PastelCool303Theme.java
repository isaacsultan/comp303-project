package JavaCool303;

import java.awt.Color;

/**
 * PastelCool303Theme has a pink primary color and orange secondary color
 * @see Cool303Theme
 * @version 1.0
 * @author isaacsultan
 */
class PastelCool303Theme extends Cool303Theme{
	public PastelCool303Theme() {
		this.setPrimaryColor(Color.PINK);
		this.setSecondaryColor(Color.ORANGE);
	}
}
